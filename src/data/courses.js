function parseJavaData(text) {
  const lines = text.split('\n');
  const modules = [];
  let currentModule = null;

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i].trim();
    if (!line) continue;

    if (line.startsWith('MODULE')) {
      if (currentModule) {
        modules.push(currentModule);
      }
      currentModule = {
        title: line,
        topics: []
      };
    } else if (line.startsWith('Learning Objectives:') || line.startsWith('Series:') || line.startsWith('Video Series:') || line.includes('Shutterstock') || line.includes('Getty Images')) {
      continue;
    } else {
      if (currentModule) {
        currentModule.topics.push({
          title: line
        });
      }
    }
  }

  if (currentModule) {
    modules.push(currentModule);
  }

  return modules;
}

